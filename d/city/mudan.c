void init()
{
        object me;

        add_action("do_order", "order");
        add_action("do_broadcast", "broadcast");
        if (query("order_owner")) {
                me = this_player();
                me->set("food", me->query("food") / 8);
                me->set("water", me->query("water") / 8);
        }
        add_action("do_get", "get");
}
int do_get(string arg)
{
	if (!arg)	return 0;
	if (arg=="all" && query("order_owner")){
		write("你是弥罗佛下凡，这么大的胃口？\n");
		return 1;
	}
	return 0;
}

int do_broadcast(string arg)
{
        string type;
        string mesg;
        object me;

        me = this_player();
        if (query("order_owner") != me->query("id"))
                return 0;
        type = query("order_type");
        if (type == "marry") {
                mesg = me->name() + "现在在醉仙楼牡丹厅举行豪门订婚酒宴了  !!!\n";
        } else if (type == "birthday") {
                mesg = me->name() + "现在在醉仙楼牡丹厅举行生辰寿宴了  !!!\n";
        } else if (type == "player") {
                mesg = me->name() + "现在在醉仙楼牡丹厅宴请各路英雄豪杰  !!!\n";
       }
        message("chat", BOLD+"【通告】"+mesg+NOR, users() );
        return 1;
}

